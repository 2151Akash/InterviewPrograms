package keyworlds;

public class ContinewKeyworld {
	
public static void main(String[] args) {
	
// When continue keyword gets execute it move the loop to the next iteration after leaving the remaining code to get execute.
	
	
	
	
		
		for (int i =0; i<=10; i++)
		{
			if(i%2==1)
			{
				continue;
			}
			
			System.out.println(i);	
			
		}
		
	}


}
