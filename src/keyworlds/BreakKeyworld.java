package keyworlds;

public class BreakKeyworld {
	public static void main(String[] args) {
		
	//	break keyword is used to terminate the loop once it get execute.
		
		for(int a=1;a<=10;a++)
		{
			if(a==5) 
			{
				System.out.println(a);
				break;
			}
			System.out.println("value is executing"+a);
		}
		}
	}



